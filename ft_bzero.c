#include <strings.h>

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*str;

	i = n;
	str = s;
	if (n > 0)
	{
		while (i > 0)
		{
			str[i - 1] = '\0';
			i--;
		}
	}
}
