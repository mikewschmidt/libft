#include "libft.h"
#include <unistd.h>

void	ft_putstr(char const *s)
{
	//int		i;

	//i = 0;
	if (!s)
		return;
	while (*s != '\0')
	{
		write(1, s, 1);
		//ft_putchar(s);
		s++;
	}
}
